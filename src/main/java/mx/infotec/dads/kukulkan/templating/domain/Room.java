package mx.infotec.dads.kukulkan.templating.domain;

/**
 * For Fiware Orion Context Broker Demo App
 * 
 * @author Daniel Cortes Pichardo
 *
 */
public class Room {
    
    private String id;
    private String entityType;
    private int order;
    private String category;
    private String name;
    private int count;
    private int temperatureValue;
    private int pressureValue;

    public int getOrder() {
        return order;
    }

    public void setOrder(int order) {
        this.order = order;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEntityType() {
        return entityType;
    }

    public void setEntityType(String entityType) {
        this.entityType = entityType;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getTemperatureValue() {
        return temperatureValue;
    }

    public void setTemperatureValue(int temperatureValue) {
        this.temperatureValue = temperatureValue;
    }

    public int getPressureValue() {
        return pressureValue;
    }

    public void setPressureValue(int pressureValue) {
        this.pressureValue = pressureValue;
    }

}
